/**
 * reset_to_98 - Updates the value of an integer to 98.
 * @n: A pointer to the integer to be updated.
 *
 * Description: This function takes a pointer to an integer and updates
 *              the value it points to with the value 98.
 *
 * Return: None.
 */

void reset_to_98(int *n)
{
	*n = 98;
}
